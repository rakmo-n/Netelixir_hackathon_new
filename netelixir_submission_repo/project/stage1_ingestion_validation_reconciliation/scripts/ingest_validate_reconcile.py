"""
Data Ingestion, Validation, and Reconciliation Pipeline
========================================================

PURPOSE
-------
Loads paid-media campaign exports (Bing/Google/Meta), GA4 session data, and
Shopify order data; normalizes everything onto a common join key (date +
channel grouping); runs a battery of validation checks -- including the
campaign-naming-consistency checks called out in the brief -- and produces a
full reconciliation comparing what the ad platforms self-report against what
GA4 sessions and Shopify orders actually show.

NOTE ON DATA SOURCES
---------------------
No real GA4 property or Shopify store was available for this project (see
the data-sourcing discussion). `load_ga4_sessions()` and `load_shopify_orders()`
below read synthetic-but-campaign-consistent CSVs generated by
`generate_synthetic_data.py` -- same dates, same channel groupings as the
real Bing/Google/Meta exports, so the join keys line up. The loaders are
written against a plain, generic schema (date/channel grouping/sessions and
order_date/channel_grouping/revenue) so swapping in a real GA4 export or a
real Shopify order export later only requires matching column names, not a
rewrite of the pipeline.

USAGE
-----
    python ingest_validate_reconcile.py \
        --bing bing_campaign_stats.csv \
        --google google_ads_campaign_stats.csv \
        --meta meta_ads_campaign_stats.csv \
        --ga4 synthetic_ga4_sessions.csv \
        --shopify synthetic_shopify_orders.csv \
        --outdir ./output

Outputs:
    output/unified_campaign_data.csv         - normalized ad-platform dataset
    output/channel_reconciliation.csv        - date+channel grain: ad vs GA4 vs Shopify
    output/reconciliation_monthly_summary.csv- ad-platform monthly totals
    output/validation_report.json            - structured findings (machine readable)
    output/validation_report.txt             - human-readable summary
"""

