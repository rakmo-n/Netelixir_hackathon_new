# ---------------------------------------------------------------------------
# 3. CAMPAIGN NAME PARSING + JOIN KEY
# ---------------------------------------------------------------------------

def parse_campaign_name(name: str) -> dict:
    """Break a campaign name into (channel_grouping, audience_tag, number).

    Returns a dict with `valid=False` if the name does not match the
    expected `<Base>[_<TM|NTM>]_Campaign_<NN>` convention at all.
    """
    m = NAME_PATTERN.match(name.strip())
    if not m:
        return {"valid": False, "base": None, "audience_tag": None, "number": None}

    prefix, num = m.group("prefix"), m.group("num")
    tag_match = AUDIENCE_SUFFIX_PATTERN.match(prefix)
    if tag_match:
        base, tag = tag_match.group("base"), tag_match.group("tag")
    else:
        base, tag = prefix, None

    return {"valid": True, "base": base, "audience_tag": tag, "number": num}


def add_derived_fields(df: pd.DataFrame) -> pd.DataFrame:
    parsed = df["campaign_name"].apply(parse_campaign_name).apply(pd.Series)
    df["channel_grouping"] = parsed["base"]
    df["audience_tag"] = parsed["audience_tag"]
    df["campaign_number"] = parsed["number"]
    df["_name_valid"] = parsed["valid"]

    # Join key: date + channel grouping. This is the cross-platform key the
    # brief calls for (date + channel grouping derived from naming/UTM).
    # Falls back to "UNPARSED" so malformed names still produce a key rather
    # than silently dropping rows from the join.
    grouping = df["channel_grouping"].fillna("UNPARSED")
    df["join_key"] = df["date"].dt.strftime("%Y-%m-%d") + "|" + grouping
    return df


# ---------------------------------------------------------------------------
# 4. VALIDATION CHECKS
# ---------------------------------------------------------------------------
# Each check returns a dict: {"check": name, "status": "pass"/"fail",
# "issue_count": n, "details": [...]}. Keeping every check in this uniform
# shape makes the report easy to scan and easy to extend.

def check_required_columns(df: pd.DataFrame) -> dict:
    missing = [c for c in UNIFIED_COLUMNS if c not in df.columns]
    return {
        "check": "required_columns_present",
        "status": "pass" if not missing else "fail",
        "issue_count": len(missing),
        "details": missing,
    }


def check_missing_critical_values(df: pd.DataFrame) -> dict:
    critical = ["date", "platform", "campaign_id", "campaign_name", "spend"]
    nulls = df[critical].isnull().sum()
    nulls = nulls[nulls > 0]
    return {
        "check": "missing_critical_values",
        "status": "pass" if nulls.empty else "fail",
        "issue_count": int(nulls.sum()),
        "details": nulls.to_dict(),
    }


def check_duplicate_grain(df: pd.DataFrame) -> dict:
    dupes = df.duplicated(subset=["platform", "campaign_id", "date"]).sum()
    return {
        "check": "duplicate_platform_campaign_date_rows",
        "status": "pass" if dupes == 0 else "fail",
        "issue_count": int(dupes),
        "details": f"{dupes} duplicate (platform, campaign_id, date) rows found",
    }


def check_negative_metrics(df: pd.DataFrame) -> dict:
    metrics = ["impressions", "clicks", "spend", "conversions"]
    issues = {}
    for m in metrics:
        n = (df[m].dropna() < 0).sum()
        if n:
            issues[m] = int(n)
    return {
        "check": "negative_metric_values",
        "status": "pass" if not issues else "fail",
        "issue_count": sum(issues.values()),
        "details": issues,
    }


def check_clicks_exceed_impressions(df: pd.DataFrame) -> dict:
    bad = df[df["clicks"] > df["impressions"]]
    return {
        "check": "clicks_exceed_impressions",
        "status": "pass" if bad.empty else "fail",
        "issue_count": len(bad),
        "details": bad[["platform", "campaign_id", "campaign_name", "date"]]
        .head(10).astype(str).to_dict(orient="records"),
    }


def check_conversions_exceed_clicks(df: pd.DataFrame) -> dict:
    bad = df[df["conversions"] > df["clicks"]]
    return {
        "check": "conversions_exceed_clicks",
        "status": "pass" if bad.empty else "warn",
        "issue_count": len(bad),
        "details": (
            f"{len(bad)} rows where conversions > clicks. This can be "
            "legitimate (multi-touch/view-through attribution) but should "
            "be confirmed against the attribution model in use."
        ),
    }


def check_campaign_naming_pattern(df: pd.DataFrame) -> dict:
    bad = df.loc[~df["_name_valid"], ["platform", "campaign_id", "campaign_name"]].drop_duplicates()
    return {
        "check": "campaign_naming_pattern_compliance",
        "status": "pass" if bad.empty else "fail",
        "issue_count": len(bad),
        "details": bad.astype(str).to_dict(orient="records"),
    }


def check_naming_delimiter_consistency(df: pd.DataFrame) -> dict:
    """Flag channel-grouping tokens that mix a space-delimited word into an
    otherwise underscore-delimited naming convention (e.g. 'Demand Gen' vs
    'Search', 'Pmax', 'Shopping')."""
    groupings = df["channel_grouping"].dropna().unique()
    inconsistent = sorted(g for g in groupings if " " in str(g))
    return {
        "check": "naming_delimiter_consistency",
        "status": "pass" if not inconsistent else "warn",
        "issue_count": len(inconsistent),
        "details": (
            f"Channel grouping token(s) {inconsistent} use a space instead "
            "of the underscore delimiter used elsewhere in the naming "
            "convention (e.g. 'Search', 'Pmax', 'Shopping')."
            if inconsistent else ""
        ),
    }


def check_id_name_one_to_one(df: pd.DataFrame) -> dict:
    """A campaign_id should map to exactly one campaign_name (and vice
    versa) within a platform. Renames/ID-reuse over time would break joins
    silently if not caught here."""
    issues = []
    for platform, sub in df.groupby("platform"):
        ids_per_name = sub.groupby("campaign_name")["campaign_id"].nunique()
        names_per_id = sub.groupby("campaign_id")["campaign_name"].nunique()
        bad_names = ids_per_name[ids_per_name > 1]
        bad_ids = names_per_id[names_per_id > 1]
        for name, n in bad_names.items():
            issues.append(f"[{platform}] name '{name}' maps to {n} different campaign_ids")
        for cid, n in bad_ids.items():
            issues.append(f"[{platform}] campaign_id '{cid}' maps to {n} different names")
    return {
        "check": "campaign_id_name_one_to_one",
        "status": "pass" if not issues else "fail",
        "issue_count": len(issues),
        "details": issues,
    }


def check_name_to_native_type_alignment(df: pd.DataFrame) -> dict:
    """For each (platform, channel_grouping), the native platform type field
    should be a single consistent value. If a channel_grouping maps to more
    than one native_channel_type within the same platform, the name and the
    underlying campaign type have drifted out of sync -- this is the
    Bing 'Demand Gen' (named) vs 'Audience' (actual type) case."""
    issues = []
    for (platform, grouping), sub in df.dropna(subset=["channel_grouping"]).groupby(
        ["platform", "channel_grouping"]
    ):
        native_types = sub["native_channel_type"].dropna().unique()
        if len(native_types) > 1:
            issues.append(
                f"[{platform}] campaigns named '{grouping}_...' have inconsistent "
                f"native types: {list(native_types)}"
            )
    return {
        "check": "name_to_native_type_alignment",
        "status": "pass" if not issues else "fail",
        "issue_count": len(issues),
        "details": issues,
    }


def check_cross_platform_type_label_drift(df: pd.DataFrame) -> dict:
    """Informational: shows, for each channel_grouping, what native type
    label each platform actually uses underneath it. Surfaces cases like
    Bing's campaigns literally named 'Demand Gen' being tagged internally
    as 'Audience' rather than a Demand-Gen-equivalent type, even though
    Google's 'Demand Gen'-named campaigns are correctly tagged DEMAND_GEN."""
    table = (
        df.dropna(subset=["channel_grouping"])
        .groupby(["channel_grouping", "platform"])["native_channel_type"]
        .agg(lambda s: sorted(s.dropna().unique().tolist()))
        .reset_index()
    )
    mismatches = []
    for grouping, sub in table.groupby("channel_grouping"):
        flat = {row["platform"]: row["native_channel_type"] for _, row in sub.iterrows()}
        mismatches.append({"channel_grouping": grouping, "native_type_by_platform": flat})
    return {
        "check": "cross_platform_type_label_drift (informational)",
        "status": "info",
        "issue_count": len(mismatches),
        "details": mismatches,
    }


def check_numbering_padding(df: pd.DataFrame) -> dict:
    """Campaign numbers should use consistent zero-padding width within a
    given (platform, channel_grouping) so sorts/joins by suffix don't break."""
    issues = []
    sub = df.dropna(subset=["channel_grouping", "campaign_number"])
    for (platform, grouping), grp in sub.groupby(["platform", "channel_grouping"]):
        widths = grp["campaign_number"].astype(str).str.len().unique()
        if len(widths) > 1:
            issues.append(
                f"[{platform}] '{grouping}' campaigns use inconsistent number "
                f"padding widths: {sorted(widths.tolist())}"
            )
    return {
        "check": "campaign_number_padding_consistency",
        "status": "pass" if not issues else "warn",
        "issue_count": len(issues),
        "details": issues,
    }


def check_date_gaps(df: pd.DataFrame) -> dict:
    """Within each campaign's active date range, flag missing calendar
    days. A handful of gaps can be normal (paused campaigns); large gaps
    are worth a second look."""
    issues = []
    for (platform, cid, name), grp in df.groupby(["platform", "campaign_id", "campaign_name"]):
        full_range = pd.date_range(grp["date"].min(), grp["date"].max(), freq="D")
        missing = full_range.difference(grp["date"])
        if len(missing) > 0:
            issues.append({
                "platform": platform,
                "campaign_id": cid,
                "campaign_name": name,
                "missing_days": len(missing),
                "active_span_days": len(full_range),
            })
    issues.sort(key=lambda x: x["missing_days"], reverse=True)
    return {
        "check": "date_coverage_gaps",
        "status": "pass" if not issues else "warn",
        "issue_count": len(issues),
        "details": issues[:15],  # top offenders only, to keep the report readable
    }


