def check_ga4_join_coverage(recon: pd.DataFrame) -> dict:
    """Of the join_keys with real ad spend (paid channels), what fraction
    have any matching GA4 session data at all?"""
    paid = recon[recon["ad_clicks"].fillna(0) > 0]
    missing = paid[paid["ga4_sessions"].isna()]
    pct = 100 * len(missing) / len(paid) if len(paid) else 0
    return {
        "check": "ga4_join_coverage",
        "status": "pass" if missing.empty else ("fail" if pct > 5 else "warn"),
        "issue_count": len(missing),
        "details": f"{len(missing)} of {len(paid)} paid (date, channel_grouping) keys "
                   f"({pct:.1f}%) have ad spend but no matching GA4 session data.",
    }


def check_shopify_join_coverage(recon: pd.DataFrame) -> dict:
    """Of the join_keys with real ad spend, what fraction have any matching
    Shopify order data at all? (Some zero-conversion days are expected and
    fine; widespread gaps point to a UTM/attribution problem.)"""
    paid = recon[recon["ad_clicks"].fillna(0) > 0]
    missing = paid[paid["shopify_orders"].isna()]
    pct = 100 * len(missing) / len(paid) if len(paid) else 0
    return {
        "check": "shopify_join_coverage",
        "status": "pass" if pct < 20 else ("fail" if pct > 60 else "warn"),
        "issue_count": len(missing),
        "details": f"{len(missing)} of {len(paid)} paid (date, channel_grouping) keys "
                   f"({pct:.1f}%) have ad spend but no matching Shopify order data.",
    }


def check_session_click_ratio_outliers(recon: pd.DataFrame) -> dict:
    """Sessions per click should sit in a plausible band (roughly 0.1x-3x).
    Values far outside that suggest a broken UTM tag, a tracking gap, or
    bot/click-fraud traffic worth investigating on a specific campaign."""
    sub = recon.dropna(subset=["session_to_click_ratio"])
    bad = sub[(sub["session_to_click_ratio"] < 0.1) | (sub["session_to_click_ratio"] > 3.0)]
    return {
        "check": "session_to_click_ratio_outliers",
        "status": "pass" if bad.empty else "warn",
        "issue_count": len(bad),
        "details": bad[["date", "channel_grouping", "ad_clicks", "ga4_sessions", "session_to_click_ratio"]]
        .head(10).astype(str).to_dict(orient="records"),
    }


