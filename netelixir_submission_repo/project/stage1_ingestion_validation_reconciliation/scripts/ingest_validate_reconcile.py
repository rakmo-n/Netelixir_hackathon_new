def build_reconciliation_summary(df: pd.DataFrame) -> pd.DataFrame:
    """Monthly totals per platform -- the baseline view you'd cross-check
    against platform UI exports or, once available, against GA4/Shopify."""
    summary = df.copy()
    summary["month"] = summary["date"].dt.to_period("M").astype(str)
    out = (
        summary.groupby(["platform", "month"])
        .agg(
            spend=("spend", "sum"),
            clicks=("clicks", "sum"),
            impressions=("impressions", "sum"),
            conversions=("conversions", "sum"),
            revenue=("revenue", "sum"),
        )
        .reset_index()
        .sort_values(["platform", "month"])
    )
    return out


# ---------------------------------------------------------------------------
# 6. GA4 SESSIONS + SHOPIFY ORDERS  (loaders + aggregation to join_key grain)
# ---------------------------------------------------------------------------
# Expected schemas (matching what generate_synthetic_data.py produces, and
# what a real export would need to provide):
#   GA4 sessions:   date, channel_grouping, sessions, avg_session_duration,
#                   engagement_rate  (platform/campaign_name/session_source_medium
#                   are carried through if present but not required)
#   Shopify orders: order_id, order_date, channel_grouping, revenue
#                   (utm_source/utm_medium/platform/campaign_name optional)
#
# Real GA4: channel_grouping would be derived from (sessionSource, sessionMedium)
# via the same kind of mapping used in generate_synthetic_data.py's
# get_source_medium(), inverted. Real Shopify: channel_grouping would come from
# parsing the order's UTM campaign field (or customer_journey_summary) the same
# way ad campaign names are parsed here.

def load_ga4_sessions(path: str) -> pd.DataFrame:
    raw = pd.read_csv(path)
    raw["date"] = pd.to_datetime(raw["date"], errors="coerce")
    required = {"date", "channel_grouping", "sessions"}
    missing = required - set(raw.columns)
    if missing:
        raise ValueError(f"GA4 sessions file is missing required columns: {missing}")
    if "join_key" not in raw.columns:
        raw["join_key"] = raw["date"].dt.strftime("%Y-%m-%d") + "|" + raw["channel_grouping"]
    return raw


def load_shopify_orders(path: str) -> pd.DataFrame:
    raw = pd.read_csv(path)
    raw["order_date"] = pd.to_datetime(raw["order_date"], errors="coerce")
    required = {"order_date", "channel_grouping", "revenue"}
    missing = required - set(raw.columns)
    if missing:
        raise ValueError(f"Shopify orders file is missing required columns: {missing}")
    if "join_key" not in raw.columns:
        raw["join_key"] = raw["order_date"].dt.strftime("%Y-%m-%d") + "|" + raw["channel_grouping"]
    return raw


def aggregate_ad_data(df: pd.DataFrame) -> pd.DataFrame:
    """Ad-platform data to join_key grain (date + channel_grouping, summed
    across platforms/campaigns that share a grouping on the same day)."""
    return (
        df.groupby("join_key")
        .agg(
            date=("date", "first"),
            channel_grouping=("channel_grouping", "first"),
            ad_clicks=("clicks", "sum"),
            ad_impressions=("impressions", "sum"),
            ad_spend=("spend", "sum"),
            ad_conversions=("conversions", "sum"),
            ad_revenue=("revenue", "sum"),
        )
        .reset_index()
    )


def aggregate_ga4_data(df: pd.DataFrame) -> pd.DataFrame:
    return (
        df.groupby("join_key")
        .agg(
            ga4_sessions=("sessions", "sum"),
            avg_session_duration=("avg_session_duration", "mean"),
            engagement_rate=("engagement_rate", "mean"),
        )
        .reset_index()
    )


def aggregate_shopify_data(df: pd.DataFrame) -> pd.DataFrame:
    return (
        df.groupby("join_key")
        .agg(
            shopify_orders=("order_id", "count") if "order_id" in df.columns else ("revenue", "count"),
            shopify_revenue=("revenue", "sum"),
        )
        .reset_index()
    )


def build_channel_reconciliation(ad_df: pd.DataFrame, ga4_df: pd.DataFrame, shopify_df: pd.DataFrame) -> pd.DataFrame:
    """The core reconciliation table: one row per (date, channel_grouping),
    with ad-platform-reported metrics sitting next to GA4 sessions and
    Shopify orders/revenue for the same key. This is what makes discrepancies
    visible -- a join_key with ad_clicks but no ga4_sessions, or ad_conversions
    that don't match shopify_orders, shows up directly as null/mismatched
    columns in one row instead of being buried in three separate files."""
    ad_agg = aggregate_ad_data(ad_df)
    ga4_agg = aggregate_ga4_data(ga4_df)
    shopify_agg = aggregate_shopify_data(shopify_df)

    merged = ad_agg.merge(ga4_agg, on="join_key", how="outer").merge(shopify_agg, on="join_key", how="outer")

    # date/channel_grouping may be null on rows that only exist in GA4/Shopify
    # (e.g. organic channels with no ad spend) -- backfill from the join_key.
    key_split = merged["join_key"].str.split("|", n=1, expand=True)
    merged["date"] = merged["date"].fillna(pd.to_datetime(key_split[0]))
    merged["channel_grouping"] = merged["channel_grouping"].fillna(key_split[1])

    merged["session_to_click_ratio"] = merged["ga4_sessions"] / merged["ad_clicks"]
    merged["conversion_gap"] = merged["ad_conversions"] - merged["shopify_orders"]
    merged["conversion_gap_pct"] = (
        merged["conversion_gap"] / merged["ad_conversions"].replace(0, pd.NA) * 100
    )
    merged["revenue_gap"] = merged["ad_revenue"] - merged["shopify_revenue"]
    merged["revenue_gap_pct"] = merged["revenue_gap"] / merged["ad_revenue"].replace(0, pd.NA) * 100

    cols = [
        "join_key", "date", "channel_grouping",
        "ad_clicks", "ad_impressions", "ad_spend", "ad_conversions", "ad_revenue",
        "ga4_sessions", "avg_session_duration", "engagement_rate", "session_to_click_ratio",
        "shopify_orders", "shopify_revenue", "conversion_gap", "conversion_gap_pct",
        "revenue_gap", "revenue_gap_pct",
    ]
    return merged[cols].sort_values(["date", "channel_grouping"]).reset_index(drop=True)


