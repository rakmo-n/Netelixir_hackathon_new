# 1. CONFIG / COMMON SCHEMA
# ---------------------------------------------------------------------------

UNIFIED_COLUMNS = [
    "date", "platform", "campaign_id", "campaign_name", "native_channel_type",
    "channel_grouping", "audience_tag", "campaign_number",
    "impressions", "clicks", "spend", "conversions", "revenue", "daily_budget",
    "join_key",
]

# Expected naming convention across all platforms:
#   <Base>[_<TM|NTM>]_Campaign_<NN>
# e.g. "Search_TM_Campaign_02", "Pmax_NTM_Campaign_11", "Generic_Campaign_01"
NAME_PATTERN = re.compile(r"^(?P<prefix>.+?)_Campaign_(?P<num>\d+)$")
AUDIENCE_SUFFIX_PATTERN = re.compile(r"^(?P<base>.+)_(?P<tag>TM|NTM)$")


