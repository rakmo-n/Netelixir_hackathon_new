def run_pipeline(bing_path: str, google_path: str, meta_path: str, outdir: str,
                  ga4_path: str = None, shopify_path: str = None):
    outdir = Path(outdir)
    outdir.mkdir(parents=True, exist_ok=True)

    frames = [
        load_bing(bing_path),
        load_google_ads(google_path),
        load_meta_ads(meta_path),
    ]
    df = pd.concat(frames, ignore_index=True)
    df = add_derived_fields(df)

    report = {"row_count": len(df), "platforms": df["platform"].value_counts().to_dict(), "checks": []}
    for check_fn in VALIDATION_CHECKS:
        result = check_fn(df)
        report["checks"].append(result)

    recon_monthly = build_reconciliation_summary(df)

    # Write unified dataset (drop internal helper column before export)
    export_df = df.drop(columns=["_name_valid"])
    export_df.to_csv(outdir / "unified_campaign_data.csv", index=False)
    recon_monthly.to_csv(outdir / "reconciliation_monthly_summary.csv", index=False)

    channel_recon = None
    if ga4_path and shopify_path:
        ga4_df = load_ga4_sessions(ga4_path)
        shopify_df = load_shopify_orders(shopify_path)
        channel_recon = build_channel_reconciliation(df, ga4_df, shopify_df)
        channel_recon.to_csv(outdir / "channel_reconciliation.csv", index=False)
        for check_fn in RECONCILIATION_CHECKS:
            report["checks"].append(check_fn(channel_recon))

    with open(outdir / "validation_report.json", "w") as f:
        json.dump(report, f, indent=2, default=str)

    write_text_report(report, outdir / "validation_report.txt")

    print(f"Unified dataset: {outdir / 'unified_campaign_data.csv'} ({len(export_df)} rows)")
    print(f"Ad-platform monthly summary: {outdir / 'reconciliation_monthly_summary.csv'}")
    if channel_recon is not None:
        print(f"Channel reconciliation (ad vs GA4 vs Shopify): {outdir / 'channel_reconciliation.csv'} "
              f"({len(channel_recon)} rows)")
    print(f"Validation report: {outdir / 'validation_report.json'} / .txt")
    n_fail = sum(1 for c in report["checks"] if c["status"] == "fail")
    n_warn = sum(1 for c in report["checks"] if c["status"] == "warn")
    print(f"Checks: {len(report['checks'])} total, {n_fail} failed, {n_warn} warnings")


def write_text_report(report: dict, path: Path):
    lines = [
        "DATA INGESTION / VALIDATION / RECONCILIATION REPORT",
        "=" * 55,
        f"Total rows ingested: {report['row_count']}",
        f"Rows by platform: {report['platforms']}",
        "",
    ]
    for c in report["checks"]:
        lines.append(f"[{c['status'].upper()}] {c['check']} -- {c['issue_count']} issue(s)")
        details = c["details"]
        if isinstance(details, list):
            for d in details[:10]:
                lines.append(f"    - {d}")
        elif isinstance(details, dict):
            for k, v in details.items():
                lines.append(f"    - {k}: {v}")
        elif details:
            lines.append(f"    - {details}")
        lines.append("")
    path.write_text("\n".join(lines))


