def check_conversion_discrepancy(recon: pd.DataFrame) -> dict:
    """Headline reconciliation metric: total ad-platform-reported conversions
    vs. total Shopify orders. Broken out by platform group rather than
    reported as one blended number, because Meta's channel groupings and
    Bing/Google's channel groupings don't overlap, and Meta's own
    'conversion' figure is several times its own click count (so it can't
    be a literal order count) -- blending it into one total would make the
    Bing/Google comparison, which is far more trustworthy, invisible."""
    bg = recon[~recon["channel_grouping"].isin(META_ONLY_GROUPINGS)]
    meta = recon[recon["channel_grouping"].isin(META_ONLY_GROUPINGS)]

    def gap(sub):
        ad, orders = sub["ad_conversions"].sum(), sub["shopify_orders"].sum()
        pct = 100 * (ad - orders) / ad if ad else 0
        return ad, orders, pct

    bg_ad, bg_orders, bg_pct = gap(bg)
    meta_ad, meta_orders, meta_pct = gap(meta)
    status = "pass" if abs(bg_pct) < 10 else ("warn" if abs(bg_pct) < 40 else "fail")

    return {
        "check": "ad_platform_vs_shopify_conversion_discrepancy",
        "status": status,
        "issue_count": 1 if status != "pass" else 0,
        "details": (
            f"Bing+Google: {bg_ad:,.0f} reported conversions vs {bg_orders:,.0f} Shopify orders "
            f"({bg_pct:+.1f}% gap). Meta: {meta_ad:,.0f} reported 'conversions' vs {meta_orders:,.0f} "
            f"Shopify orders ({meta_pct:+.1f}% gap) -- Meta's figure is not comparable to the others "
            f"(it exceeds Meta's own click count), so its gap is reported separately rather than "
            f"blended into one misleading total."
        ),
    }


def check_revenue_discrepancy(recon: pd.DataFrame) -> dict:
    """Same idea for revenue, restricted to channel groupings where the ad
    platform actually reports a revenue figure (Bing and Google do; Meta's
    export has no revenue field at all, so it's naturally excluded here --
    every channel grouping with a non-null ad_revenue is Bing/Google)."""
    sub = recon.dropna(subset=["ad_revenue"])
    total_ad_rev = sub["ad_revenue"].sum()
    total_shop_rev = sub["shopify_revenue"].sum()
    gap = total_ad_rev - total_shop_rev
    pct = 100 * gap / total_ad_rev if total_ad_rev else 0
    status = "pass" if abs(pct) < 10 else ("warn" if abs(pct) < 40 else "fail")
    return {
        "check": "ad_platform_vs_shopify_revenue_discrepancy",
        "status": status,
        "issue_count": 1 if status != "pass" else 0,
        "details": (
            f"Bing+Google (the only platforms with a revenue field): ad platforms report "
            f"${total_ad_rev:,.2f}, Shopify shows ${total_shop_rev:,.2f} ({pct:+.1f}% gap)."
        ),
    }


RECONCILIATION_CHECKS = [
    check_ga4_join_coverage,
    check_shopify_join_coverage,
    check_session_click_ratio_outliers,
    check_conversion_discrepancy,
    check_revenue_discrepancy,
]


